import UIKit

extension UIColor {
    
    class var MPGrayColor: UIColor {
        return UIColor(red: 64/255, green: 64/255, blue: 64/255, alpha: 1)
    }
    
    class var MPLightGrayColor: UIColor {
        return UIColor(red: 128/255, green: 128/255, blue: 128/255, alpha: 1)
    }
    
    class var MPBackgroundColor: UIColor {
        return UIColor(red: 224/255, green: 224/255, blue: 224/255, alpha: 1)
    }
    
    class var MPOrangeColor: UIColor {
        return UIColor(red: 245/255, green: 200/255, blue: 125/255, alpha: 1)
    }
    
}
